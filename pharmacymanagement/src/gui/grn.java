    public static void total() {
        try {
            Double total = 0.0;
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            for (int j = 0; j < jTable1.getRowCount(); j++) {
                Double y = Double.parseDouble(dtm.getValueAt(j, 8).toString());
                Double[] in = new Double[]{y};

                for (int k = 0; k < in.length; k++) {

                    Double l = in[k];

                    total = total + l;
                }

            }
            jLabel7.setText(total + "");

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void ato() {
        try {
            Statement s = database.database.pharmacy().createStatement();
            ResultSet rs = s.executeQuery("SELECT MAX(grn_id) FROM grn");
            while (rs.next()) {
                int i = 0;
                i = rs.getByte("MAX(grn_id)");
                jTextField5.setText(++i + "");

            }
        } catch (Exception e) {
            e.printStackTrace();

        }
    }
