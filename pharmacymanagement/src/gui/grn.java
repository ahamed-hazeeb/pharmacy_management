    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            Statement s = database.database.pharmacy().createStatement();

            ResultSet rs = s.executeQuery("SELECT * FROM grn WHERE grn_id = '" + jTextField5.getText() + "'");
            while (rs.next()) {
                jTextField5.setText(rs.getString(1));
                jTextField6.setText(rs.getString(2));
                jDateChooser1.setDate(rs.getDate(3));
                jLabel7.setText(rs.getString(4));
            }

            ResultSet rs2 = s.executeQuery("SELECT stock.stock_id, stock.med_id, medicine.name, medicine.type, stock.mfdate, stock.exdate, medicine.buy_pr, stock.quantity, stock.amount FROM stock INNER JOIN medicine ON stock.med_id=medicine.med_id WHERE stock.grn_id = '" + jTextField5.getText() + "'");
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);

            while (rs2.next()) {
                Vector v = new Vector();
                v.add(rs2.getString("stock_id"));
                v.add(rs2.getString("med_id"));
                v.add(rs2.getString("name"));
                v.add(rs2.getString("type"));
                v.add(rs2.getString("mfdate"));
                v.add(rs2.getString("exdate"));
                v.add(rs2.getString("buy_pr"));
                v.add(rs2.getString("quantity"));
                v.add(rs2.getString("amount"));
                df.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

