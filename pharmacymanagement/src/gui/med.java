    private void auto() {
        try {

            Statement s = database.database.pharmacy().createStatement();
            ResultSet rs = s.executeQuery("SELECT MAX(med_id) FROM medicine");
            while (rs.next()) {
                int i = 0;
                i = rs.getByte("MAX(med_id)");

                txtadmedid.setText(++i + "");

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
