    private void txtadmednameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtadmednameKeyReleased
        if (txtadmedname.getText().isEmpty()) {
            jPanel1.setVisible(false);
        } else {
            jPanel1.setVisible(true);
            try {
                Statement s = database.database.pharmacy().createStatement();
                ResultSet rs = s.executeQuery("SELECT * FROM medicine WHERE name LIKE'" + txtadmedname.getText() + "%'");
                Vector v = new Vector();
                if (rs.next()) {
                    v.add(rs.getString("name"));
                } else {
                    jPanel1.setVisible(false);
                }
                jList1.setModel(new DefaultComboBoxModel(v));

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_txtadmednameKeyReleased

