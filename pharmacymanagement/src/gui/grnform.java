    private void autoincrement() {
        try {
            Statement s = database.database.pharmacy().createStatement();

            ResultSet rs = s.executeQuery("SELECT MAX(stock_id) FROM stock");
            while (rs.next()) {
                id = 0;
                id = rs.getInt("MAX(stock_id)");
                jLabel9.setText(++id + "");
            }
            

            DefaultTableModel df = (DefaultTableModel) grn.jTable1.getModel();
            int i = grn.jTable1.getRowCount();
            if (i > 0) {
                i = --i;
                id = Integer.parseInt(df.getValueAt(i, 0).toString());
                jLabel9.setText(++id + "");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
