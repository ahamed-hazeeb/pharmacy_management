    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DefaultTableModel df = (DefaultTableModel) grn.jTable1.getModel();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

            java.util.Date d1 = jDateChooser1.getDate();
            String s1 = sdf.format(d1);

            java.util.Date d2 = jDateChooser2.getDate();
            String s2 = sdf.format(d2);

            java.util.Date d3 = new Date();
            String s3 = sdf.format(d3);

            DateTime dt1 = new DateTime(d2);
            DateTime dt2 = new DateTime(d3);

            Days d = Days.daysBetween(dt2, dt1);
            long dt = d.getDays();

            if (dt > 183) {
                Vector v = new Vector();
                v.add(jLabel9.getText());
                v.add(jTextField1.getText());
                v.add(jTextField2.getText());
                v.add(jComboBox3.getSelectedItem().toString());
                v.add(s1);
                v.add(s2);
                v.add(jTextField4.getText());
                v.add(jTextField3.getText());
                v.add(jLabel12.getText());
                df.addRow(v);

                jLabel9.setText(++id + "");
                jTextField1.setText("");
                jTextField2.setText("");
                jComboBox3.setSelectedIndex(0);
                jDateChooser1.setDate(null);
                jDateChooser2.setDate(null);
                jTextField4.setText("");
                jTextField3.setText("");
                jLabel12.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Unacceptable Expiary Date");
                jDateChooser2.setBackground(Color.red);
            }


        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

